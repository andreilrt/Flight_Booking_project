package Modelo;

public class Ciudad {
    protected String id_Nom_Ciudad;
    protected String Pais;

    public String getId_Nom_Ciudad() {
        return id_Nom_Ciudad;
    }

    public void setId_Nom_Ciudad(String id_Nom_Ciudad) {
        this.id_Nom_Ciudad = id_Nom_Ciudad;
    }

    public String getPais() {
        return Pais;
    }

    public void setPais(String Pais) {
        this.Pais = Pais;
    }
    
    
}
